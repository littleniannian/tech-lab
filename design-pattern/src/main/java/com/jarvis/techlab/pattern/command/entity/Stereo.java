package com.jarvis.techlab.pattern.command.entity;

public class Stereo {

    public void on() {
        System.out.println("stereo is on!");
    }

    public void off() {
        System.out.println("stereo is off!");
    }

    public void setCD() {
        System.out.println("CD is in stereo!");
    }

    public void setDVD() {
        System.out.println("DVD is in stereo!");
    }

    public void setRadio() {
        System.out.println("Radio is set!");
    }

    public void setVolume() {
        System.out.println("Volume is set!");
    }

}
